package com.techelevator.demo;

public class Item {

    // Defining member variables

    private double price;
    private String name;
    private int sku;
    private String description;

    // Getters

    public double getPrice(){
        return price;
    }

    public String getName(){
        return name;
    }

    public int getSku(){
        return sku;
    }

    public String getDescription(){
        return description;
    }

}
